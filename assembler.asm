############### The loop fits in cache ########################################
###############################################################################

#Sum all integers from 0 to 10

addi $1 $0 0 # count = 10
addi $11 $0 11
add $2 $0 $0 				#Initialize sum
addi $3 $0 100 				#Mem location

LOOP: addi $1 $1 1
beq $1 $11 LOAD

add $2 $1 $2
sw $2 0($3) 				#Store at 100 + 4 * i
addi $3 $3 4
j LOOP

LOAD: lw $7 -4($3)

END: j END